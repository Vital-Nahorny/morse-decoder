const MORSE_TABLE = {
    '': ' ',
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let str = expr.match(/.{1,10}/g);
    let arr = [];
    let a = '';
  
    for (let i = 0; i < str.length; i++) {
      arr.push(str[i].replace(/11/g, "-").replace(/10/g, ".").replace(/00/g, "").replace(/\*/g, ""));
    }
  
    for (let i = 0; i < arr.length; i++) {
      a += `${MORSE_TABLE[arr[i]]}`;
    }
 
    return a;
}

module.exports = {
    decode
}